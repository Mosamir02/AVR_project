/***********************************_FUNCTIONS_*********************************/
void H_TempSensor_Inint(void)
{
	M_Adc_Init();
}
u16 H_TempSensor_Read(void)
{
	u16 Local_u16_AdcValue =  M_Adc_Read();
	u16 Local_u16_Temp	   = ((u32)Local_u16_AdcValue * 500) / 1023;
	return Local_u16_Temp;
}
