/***********************************_INCLUDES_***********************************/
#include "Wdt.h"
/***********************************_FUNCTIONS_**********************************/
void M_Wdt_Init(void)
{
	//TO ENABLE WDT
	SET_BIT(WDTCR,3);
	//to select window time  = 2.1 seconds
	SET_BIT(WDTCR,0);
	SET_BIT(WDTCR,1);
	SET_BIT(WDTCR,2);
}

void M_Wdt_Refresh(void)
{
	asm("WDR");
}
